    public ArrayList<Hotel> orderHotels(){
        ArrayList<Hotel> results = new ArrayList();
        ArrayList<Hotel> clone = (ArrayList<Hotel>)global.getHotelsList().clone();
        if(clone.size() > 0){
            while(clone.size() > 0){
                int index = 0, high = 0;
                for(Hotel temporalHotel : clone){
                    if(temporalHotel.getReserveCount() > clone.get(high).getReserveCount()){
                        high = index;
                    }
                    index++;
                }
                results.add(clone.get(high));
                clone.remove(high);
            }
        }
        return results;
    }
    
