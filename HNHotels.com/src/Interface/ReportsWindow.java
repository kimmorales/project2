    private void chargeComboSeason(Hotel hotel){
        jComboBoxSeasonHotel.removeAllItems();
        for(Season temporalSeason : hotel.getSeasons()){
            if(hotel.getSeasons().isEmpty())
                return;
            else            
            jComboBoxSeasonHotel.addItem(temporalSeason.getCode());
            
        }
    }
