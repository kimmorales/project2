    public void chargeCancelReserves(){
        DefaultListModel model = new DefaultListModel();
        for(Reserve temporalReserve : global.getCancelReservationsList()){
            model.addElement(temporalReserve.getCode());
        }
        jListCancelReserves.setModel(model);
    }
    public void chargeCompleteReserves(){
        DefaultListModel model = new DefaultListModel();
        for(Reserve temporalReserve :  global.getCompleteReservationList()){
            model.addElement(temporalReserve.getCode());
        }
        jListCompleteReserves.setModel(model);
    }
    public void chargeHotelsForReserves(ArrayList<Hotel> list){
        DefaultListModel model = new DefaultListModel();
        for(Hotel temporalHotel : list){
            model.addElement(temporalHotel.getName()+" -Amount:"+temporalHotel.getNumberOfReservations());
        }
        jListHotelForReservations.setModel(model);
    }
    public void chargeComboWithHotels(){
        for(Hotel temporalHotel : global.getHotelsList()){
            jComboBoxHotelReport.addItem(temporalHotel.getName());
        }
    }
    
